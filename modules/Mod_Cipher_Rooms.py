def checkRoomsAvailability(day, hours, checkIn):
    roomsAvailable = []
    rooms_agenda = get_roomsAgenda()  # Toma la información de disponibilidad de cuartos de la DB

    # Se ajustan los parámetros para utilizarlos en matrices
    checkOut = checkIn + hours + 1
    day = day - 1

    for x_room in range(0, len(rooms_agenda)):
        if checkOut <= 23:  # Si la reservación es durante el mismo días
            val_duration = numpy.zeros(24)  # Matriz para verificar los espacios disponibles
            for duration in range(checkIn, checkOut):
                if rooms_agenda[x_room][day][duration][0] == "1":
                    val_duration[duration] = 1
            isAvailable = True
            for checking in range(len(val_duration)):
                if val_duration[checking] == 1:
                    isAvailable = False
            if isAvailable == True:
                roomsAvailable.insert(len(roomsAvailable), (x_room + 1))
        if checkOut >= 24:  # Si la reservación abarca parte de 2 días
            val_duration = numpy.zeros(48)  # Matriz para verificar los espacios disponibles
            for duration in range(checkIn, checkOut):
                if duration <= 23:
                    if rooms_agenda[x_room][day][duration][0] == "1":
                        val_duration[duration] = 1
                if duration >= 24:
                    if rooms_agenda[x_room][day + 1][duration - 24][0] == "1":
                        val_duration[duration] = 1

            isAvailable = True
            for checking in range(len(val_duration)):
                if val_duration[checking] == 1:
                    isAvailable = False
            if isAvailable == True:
                roomsAvailable.insert(len(roomsAvailable), (x_room + 1))

    return roomsAvailable


