def parseRoomAgenda(rooms_agenda):
    new_rooms_agenda = ""
    # Recorre el arreglo
    for xroom in range(len(rooms_agenda)):
        if xroom + 1 <= 9: # Si xroom es menor de 10 agrega un cero al nombre del cuarto
            new_rooms_agenda += f"[R0{xroom + 1}]="
        elif xroom + 1 > 9:
            new_rooms_agenda += f"[R{xroom + 1}]="
        for day in range(len(rooms_agenda[xroom])):
            for hour in range(len(rooms_agenda[xroom][day])):
                new_rooms_agenda += str(rooms_agenda[xroom][day][hour][0])
                if hour < len(rooms_agenda[xroom][day]) - 1:
                    new_rooms_agenda += "|"  # Añade el separador de horas
            if day < len(rooms_agenda[xroom]) - 1:
                new_rooms_agenda += "/"  # Añade el separador de días

    return new_rooms_agenda
